// If c is a printable character and its glyph has a pixel set at position (x,y)
// returns 1 and returns 0 otherwise
int64_t hasPixel(char c, uint64_t x, uint64_t y) {
  // If c is not  printable character return 0
  if (c < ' ' || c > '~') {
    return 0;
  }
  if (x >= AERIAL_FONT_WIDTH) {
    return 0;
  }
  if (y >= AERIAL_FONT_HEIGHT) {
    return 0;
  }

  uint64_t index = c - ' ';

  return ((aerialGlyphArray[index][y] >> (AERIAL_FONT_WIDTH - 1 - x)) & 0x1);
}
